using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using YAMCqrs.Core.SourceGeneration.Info;

namespace YAMCqrs.Core.SourceGeneration.GeneratorHelper;

internal static class DispatcherHelper
{
    private static StringBuilder _sb;

    public static string GenerateCode(List<HandlerInfo> handlers)
    {
        _sb = new StringBuilder();

        // Collect all unique commands and queries with their result types
        var allCommands = handlers
            .SelectMany(h => h.CommandTypes)
            .Distinct()
            .ToList();

        var allQueries = handlers
            .SelectMany(h => h.QueryTypes)
            .Distinct()
            .ToList();

        _sb.AppendLine("// <auto-generated />");
        _sb.AppendLine($"// Generation Date: {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC");
        _sb.AppendLine("#nullable enable");
        _sb.AppendLine("using System;");
        _sb.AppendLine("using System.Linq;");
        _sb.AppendLine("using System.Threading;");
        _sb.AppendLine("using System.Threading.Tasks;");
        _sb.AppendLine("using System.Transactions;");
        _sb.AppendLine("using System.Collections.Concurrent;");
        _sb.AppendLine("using System.Collections.Generic;");
        _sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        _sb.AppendLine(Const.Usings.ICommand);
        _sb.AppendLine(Const.Usings.IQuery);
        _sb.AppendLine(Const.Usings.IDispatcher);

        _sb.AppendLine();
        _sb.AppendLine("namespace YAMCqrs.Core;");
        _sb.AppendLine();
        _sb.AppendLine("/// <summary>");
        _sb.AppendLine("/// Initializes a new instance of the <see cref=\"Dispatcher\"/> class.");
        _sb.AppendLine("/// </summary>");
        _sb.AppendLine("/// <param name=\"_serviceProvider\">The service provider.</param>");
        _sb.AppendLine("internal sealed class Dispatcher(IServiceProvider _serviceProvider) : IDispatcher");
        _sb.AppendLine("{");

        GenerateCacheDictionary(allCommands, allQueries);
        _sb.AppendLine();
        if (allCommands.Count > 0)
        {
            GenerateGetOrderedCommandInterceptorsMethod();
        }
        else
        {
            _sb.AppendLine("    /// <summary>");
            _sb.AppendLine("    /// No command handlers registered - skipping command interceptor method generation");
            _sb.AppendLine("    /// </summary>");
        }
        _sb.AppendLine();
        if (allQueries.Count > 0)
        {
            GenerateGetOrderedQueryInterceptorsMethod();
        }
        else
        {
            _sb.AppendLine("    /// <summary>");
            _sb.AppendLine("    /// No query handlers registered - skipping query interceptor method generation");
            _sb.AppendLine("    /// </summary>");
        }
        _sb.AppendLine();
        GenerateSendAsyncMethod(allCommands);
        _sb.AppendLine();
        GenerateQueryAsyncMethod(allQueries);

        _sb.Append("}");

        return _sb.ToString();
    }

    private static void GenerateCacheDictionary(List<(string CommandType, string ResultType)> allCommands, List<(string QueryType, string ResultType)> allQueries)
    {
        if (allCommands.Count == 0 && allQueries.Count == 0)
            return;

        _sb.AppendLine("    /// <summary>");
        _sb.AppendLine("    /// Cache only the sorted type order — no live instances stored");
        _sb.AppendLine("    /// </summary>");
        _sb.AppendLine("    private static readonly ConcurrentDictionary<Type, Type[]> _interceptorTypeOrderCache = new();");
    }

    private static void GenerateGetOrderedCommandInterceptorsMethod()
    {
        _sb.AppendLine("    /// <summary>");
        _sb.AppendLine("    /// Resolves fresh interceptor instances per scope, using cached type sort order");
        _sb.AppendLine("    /// </summary>");
        _sb.AppendLine("    private IReadOnlyList<ICommandInterceptor<TCommand, TResult>> GetOrderedCommandInterceptors<TCommand, TResult>() where TCommand : ICommand<TResult>");
        _sb.AppendLine("    {");

        _sb.AppendLine("        //Always resolve FRESH instances from the current scope");
        _sb.AppendLine("        var interceptors = _serviceProvider");
        _sb.AppendLine("        .GetServices<ICommandInterceptor<TCommand, TResult>>()");
        _sb.AppendLine("        .ToList();");
        _sb.AppendLine("        if (interceptors.Count <= 1)");
        _sb.AppendLine("            return interceptors;");
        _sb.AppendLine("        var cacheKey = typeof(ICommandInterceptor<TCommand, TResult>);");
        _sb.AppendLine("        //Cache only the sorted Type[] — types are static, instances are not");
        _sb.AppendLine("        var sortedTypes = _interceptorTypeOrderCache.GetOrAdd(cacheKey, _ =>");
        _sb.AppendLine("        {");

        _sb.AppendLine("            var copy = interceptors.ToList();");
        _sb.AppendLine("            copy.Sort((a, b) =>");
        _sb.AppendLine("            {");
        _sb.AppendLine("                var layerComp = ((int)a.Layer).CompareTo((int)b.Layer);");
        _sb.AppendLine("                return layerComp != 0 ? layerComp : a.Order.CompareTo(b.Order);");
        _sb.AppendLine("            });");
        _sb.AppendLine("            return copy.Select(i => i.GetType()).ToArray();");
        _sb.AppendLine("        });");

        _sb.AppendLine("        //Return fresh instances ordered by the cached type sort");
        _sb.AppendLine("        return sortedTypes");
        _sb.AppendLine("        .Select(t => interceptors.First(i => i.GetType() == t))");
        _sb.AppendLine("        .ToList();");

        _sb.AppendLine("    }");
    }

    private static void GenerateGetOrderedQueryInterceptorsMethod()
    {
        _sb.AppendLine("    /// <summary>");
        _sb.AppendLine("    /// Resolves fresh interceptor instances per scope, using cached type sort order");
        _sb.AppendLine("    /// </summary>");
        _sb.AppendLine("    private IReadOnlyList<IQueryInterceptor<TQuery, TResult>> GetOrderedQueryInterceptors<TQuery, TResult>() where TQuery : IQuery<TResult>");

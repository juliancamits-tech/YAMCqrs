        _sb.AppendLine("    .Select(t => interceptors.First(i => i.GetType() == t))");
        _sb.AppendLine("    .ToList();");

        _sb.AppendLine("    }");
    }

    private static void GenerateSendAsyncMethod(List<(string CommandType, string ResultType)> allCommands)
    {
        _sb.AppendLine("    public async Task<Result<TResult>> SendAsync<TResult>(ICommand<TResult> command, CancellationToken cancellationToken = default)");
        _sb.AppendLine("    {");

        if (allCommands.Count > 0)
        {
            _sb.AppendLine("    // Type-safe dispatch with cached interceptor pipeline");
            _sb.AppendLine("    switch (command)");
            _sb.AppendLine("    {");
            for (int i = 0; i < allCommands.Count; i++)
            {
                var (commandType, resultType) = allCommands[i];
                var typeCommand = $"typedCommand{i}";

                var caseItem = $@"      case {commandType} {typeCommand}:
        {{
            // Create ambient transaction scope
            using var transactionScope = new TransactionScope(
            TransactionScopeOption.Required,
            TransactionScopeAsyncFlowOption.Enabled);

            var orderedInterceptors = GetOrderedCommandInterceptors<{commandType}, {resultType}>();
            var handler = _serviceProvider.GetRequiredService<ICommandHandler<{commandType}, {resultType}>>();

            // Tracks how many interceptors completed OnBefore — used for correct OnError unwinding
            var executedBeforeCount = 0;

            try
            {{
                // Execute OnBefore interceptors (ascending order)
                foreach (var interceptor in orderedInterceptors)
                {{
                    await interceptor.OnBeforeAsync({typeCommand}, cancellationToken).ConfigureAwait(false);
                    executedBeforeCount++;
                }}

                // Execute handler (can publish events, which will enlist in ambient transaction)
                var result = await handler.HandleAsync({typeCommand}, cancellationToken).ConfigureAwait(false);

                // Execute OnAfter interceptors (descending order - like a stack)
                for (int idx = orderedInterceptors.Count - 1; idx >= 0; idx--)
                    await orderedInterceptors[idx].OnAfterAsync({typeCommand}, result, cancellationToken).ConfigureAwait(false);

                // Complete transaction - triggers event commit
                transactionScope.Complete();

                return (Result<TResult>)(object)result;
            }}
            catch (Exception ex)
            {{
                // Don't call Complete() — automatic rollback
                // Only unwind interceptors that actually completed OnBefore (reverse order = stack unwinding)
                // e.g. if OnBefore[2] threw, only [1] and [0] get OnError — [2..n] were never entered
                for (int idx = executedBeforeCount - 1; idx >= 0; idx--)
                    await orderedInterceptors[idx].OnErrorAsync({typeCommand}, ex, cancellationToken).ConfigureAwait(false);
                throw;
            }}
            // TransactionScope.Dispose() is called here
            // - If Complete() was called: Commit (events persisted)
            // - If Complete() was NOT called: Rollback (events discarded)
        }}";
                _sb.AppendLine(caseItem);
            }

            _sb.AppendLine("        default:");
            _sb.AppendLine($"           throw new InvalidOperationException($\"No handler registered for command type {{command.GetType().Name}}\");");
        }
        else
        {
            _sb.AppendLine("        throw new InvalidOperationException(\"No command handlers registered\");");
        }

        _sb.AppendLine("    }");
        _sb.AppendLine("}");
    }

    private static void GenerateQueryAsyncMethod(List<(string QueryType, string ResultType)> allQueries)
    {
        _sb.AppendLine("    public async Task<Result<TResult>> QueryAsync<TResult>(IQuery<TResult> query, CancellationToken cancellationToken = default)");
        _sb.AppendLine("    {");

        if (allQueries.Count > 0)
        {
            _sb.AppendLine("    //Type-safe dispatch with cached interceptor pipeline");
            _sb.AppendLine("        switch (query)");
            _sb.AppendLine("        {");

            for (int i = 0; i < allQueries.Count; i++)
            {
                var (QueryType, ResultType) = allQueries[i];
                var typedQuery = $"typedQuery{i}";

                var caseItem = $@"      case {QueryType} {typedQuery}:
        {{
            var orderedInterceptors = GetOrderedQueryInterceptors<{QueryType}, {ResultType}>();
            var handler = _serviceProvider.GetRequiredService<IQueryHandler<{QueryType}, {ResultType}>>();

            // Tracks how many interceptors completed OnBefore — used for correct OnError unwinding
            var executedBeforeCount = 0;

            try
            {{
                // Execute OnBefore interceptors (ascending order)
                foreach (var interceptor in orderedInterceptors)
                {{
                    await interceptor.OnBeforeAsync({typedQuery}, cancellationToken).ConfigureAwait(false);
                    executedBeforeCount++;
                }}

                // Execute handler
                var result = await handler.HandleAsync({typedQuery}, cancellationToken).ConfigureAwait(false);

                // Execute OnAfter interceptors (descending order - like a stack)
                for (int idx = orderedInterceptors.Count - 1; idx >= 0; idx--)
                    await orderedInterceptors[idx].OnAfterAsync({typedQuery}, result, cancellationToken).ConfigureAwait(false);

                return (Result<TResult>)(object)result;
            }}
            catch (Exception ex)
            {{
                // Only unwind interceptors that actually completed OnBefore (reverse order = stack unwinding)
                // e.g. if OnBefore[2] threw, only [1] and [0] get OnError — [2..n] were never entered
                for (int idx = executedBeforeCount - 1; idx >= 0; idx--)
                    await orderedInterceptors[idx].OnErrorAsync({typedQuery}, ex, cancellationToken).ConfigureAwait(false);
                throw;
            }}
        }}";

                _sb.AppendLine(caseItem);
            }
            _sb.AppendLine("    default:");
            _sb.AppendLine("    throw new InvalidOperationException($\"No handler registered for query type {query.GetType().Name}\");");
        }
        else
        {
            _sb.AppendLine("        throw new InvalidOperationException(\"No query handlers registered\");");
        }

        _sb.AppendLine("    }");
        _sb.AppendLine("}");
    }

}
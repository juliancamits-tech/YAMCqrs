        _sb.AppendLine("            return interceptors;");
        _sb.AppendLine("        var cacheKey = typeof(IQueryInterceptor<TQuery, TResult>);");
        _sb.AppendLine("        //Cache only the sorted Type[] — types are static, instances are not");
        _sb.AppendLine("        var sortedTypes = _interceptorTypeOrderCache.GetOrAdd(cacheKey, _ =>");
        _sb.AppendLine("        {");
        _sb.AppendLine("            var copy = interceptors.ToList();");
        _sb.AppendLine("            copy.Sort((a, b) =>");
        _sb.AppendLine("            {");
